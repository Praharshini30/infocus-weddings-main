const BASE_URL = 'https://res.cloudinary.com/dtfbshx24/image/upload';

/**
 * Returns optimized Cloudinary URL for a given local file path/name.
 * Falls back to local path if no mapping is found.
 * 
 * @param {string} localPath - e.g. "/assets/WMD08878_hm3fhy.jpg", "WMD08878", etc.
 * @param {number} width - Target width: 600 (card), 1200 (gallery), 1600 (hero)
 */
export function getCloudinaryUrl(localPath, width = 1200) {
  if (!localPath) return '';
  
  // Extract filename base
  let cleanName = localPath;
  if (localPath.includes('/')) {
    cleanName = localPath.substring(localPath.lastIndexOf('/') + 1);
  }
  if (cleanName.includes('.')) {
    cleanName = cleanName.substring(0, cleanName.lastIndexOf('.'));
  }
  
  // Clean special separators to match keys
  cleanName = cleanName.replace(/%20/g, ' ');
  // Convert standard variations to key matching style
  const possibleKeys = [
    cleanName,
    cleanName.replace(/\s+/g, '_'),
    cleanName.replace(/\s+/g, '-'),
    cleanName.replace(/-/g, '_'),
    cleanName.replace(/_/g, ' '),
    cleanName.replace(/ - /g, ' - ')
  ];

  let mappedAsset = null;
  for (const key of possibleKeys) {
    // Normal case-insensitive search
    const matchedKey = Object.keys(CLOUDINARY_MAPPING).find(
      (k) => k.toLowerCase() === key.toLowerCase() || k.replace(/\s+/g, '').toLowerCase() === key.replace(/\s+/g, '').toLowerCase()
    );
    if (matchedKey) {
      mappedAsset = CLOUDINARY_MAPPING[matchedKey];
      break;
    }
  }

  if (!mappedAsset) {
    // Fallback to local path if not in Cloudinary list
    return localPath;
  }

  // Inject width, f_auto, and q_auto
  return `${BASE_URL}/f_auto,q_auto,w_${width}/${mappedAsset}`;
}
